class Shape {

	private String name;
	private Integer perimeter, area;

	// Getter
	public String getName() {
		return name;
	}

	public Integer getPermiter() {
		return perimeter;
	}

	public Integer getArea() {
		return area;
	}

	// Setter
	public void setName(String newName) {
		name = newName;
	}
}
